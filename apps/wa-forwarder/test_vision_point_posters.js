/**
 * Test batch poster generation
 */
async function testBatchPosterGeneration() {
  console.log('\n🧪 Testing Batch Poster Generation');
  console.log('=' .repeat(50));
  
  try {
    console.log('📁 Generating posters for first 3 scripts...');
    
    // Get first 3 scripts for testing
    const scriptFiles = await fs.readdir(SCRIPTS_DIR);
    const testScripts = scriptFiles
      .filter(file => file.endsWith('.txt'))
      .slice(0, 3);
    
    if (testScripts.length === 0) {
      console.log('❌ No scripts found for batch testing');
      return false;
    }
    
    console.log(`📋 Testing with ${testScripts.length} scripts:`);
    testScripts.forEach((script, index) => {
      console.log(`   ${index + 1}. ${script}`);
    });
    
    // Create temporary directory with test scripts
    const tempDir = path.join(__dirname, 'temp_test_scripts');
    await fs.ensureDir(tempDir);
    
    // Copy test scripts
    for (const script of testScripts) {
      const sourcePath = path.join(SCRIPTS_DIR, script);
      const destPath = path.join(tempDir, script);
      await fs.copy(sourcePath, destPath);
    }
    
    // Generate posters
    const results = await generatePostersForAllScripts(tempDir, {
      style: 'news',
      generateBackground: false // Skip background generation for faster testing
    });
    
    // Clean up temp directory
    await fs.remove(tempDir);
    
    const successCount = results.filter(r => r.success).length;
    console.log(`✅ Batch generation completed: ${successCount}/${results.length} successful`);
    
    return successCount > 0;
    
  } catch (error) {
    console.error(`❌ Batch poster generation test failed: ${error.message}`);
    return false;
  }
}

