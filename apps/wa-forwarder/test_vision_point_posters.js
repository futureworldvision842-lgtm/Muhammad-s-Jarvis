/**
 * Test Vision Point Poster Generation System
 * Tests poster creation from existing scripts with different styles
 */

const path = require('path');
const fs = require('fs-extra');
const {
  generatePosterFromScript,
  generatePostersForAllScripts,
  generatePosterContentAI,
  extractPosterContent
} = require('./vision_point_poster_generator');

// Test configuration
const SCRIPTS_DIR = path.join(__dirname, 'generated_scripts');
const OUTPUT_DIR = path.join(__dirname, 'output', 'posters');

/**
 * Test single poster generation
 */
async function testSinglePosterGeneration() {
  console.log('🧪 Testing Single Poster Generation');
  console.log('=' .repeat(50));
  
  try {
    // Find a sample script
    const scriptFiles = await fs.readdir(SCRIPTS_DIR);
    const urduScripts = scriptFiles.filter(file => 
      file.includes('urdu') && file.endsWith('.txt')
    );
    
    if (urduScripts.length === 0) {
      console.log('❌ No Urdu scripts found for testing');
      return false;
    }
    
    const testScript = urduScripts[0];
    const scriptPath = path.join(SCRIPTS_DIR, testScript);
    
    console.log(`📄 Testing with script: ${testScript}`);
    
    // Test different poster styles
    const styles = ['news', 'breaking', 'analysis', 'feature'];
    
    for (const style of styles) {
      console.log(`\n🎨 Testing ${style} style poster...`);
      
      const result = await generatePosterFromScript(scriptPath, {
        style: style,
        generateBackground: true
      });
      
      if (result.success) {
        console.log(`✅ ${style} poster generated successfully!`);
        console.log(`📁 File: ${result.filename}`);
      } else {
        console.log(`❌ ${style} poster failed: ${result.error}`);
      }
    }
    
    return true;
    
  } catch (error) {
    console.error(`❌ Single poster test failed: ${error.message}`);
    return false;
  }
}

