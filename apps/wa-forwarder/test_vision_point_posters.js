/**
 * Run comprehensive poster generation tests
 */
async function runComprehensiveTests() {
  console.log('🎨 VISION POINT POSTER GENERATION TESTS');
  console.log('=' .repeat(60));
  console.log('Testing comprehensive poster generation system...\n');
  
  const tests = [
    { name: 'Output Structure', fn: testOutputStructure },
    { name: 'Content Extraction', fn: testContentExtraction },
    { name: 'AI Poster Generation', fn: testAIPosterGeneration },
    { name: 'Single Poster Generation', fn: testSinglePosterGeneration },
    { name: 'Batch Poster Generation', fn: testBatchPosterGeneration }
  ];
  
  const results = [];
  
  for (const test of tests) {
    try {
      console.log(`\n🧪 Running ${test.name} test...`);
      const result = await test.fn();
      results.push({ name: test.name, success: result });
      
      if (result) {
        console.log(`✅ ${test.name} test PASSED`);
      } else {
        console.log(`❌ ${test.name} test FAILED`);
      }
    } catch (error) {
      console.error(`❌ ${test.name} test ERROR: ${error.message}`);
      results.push({ name: test.name, success: false, error: error.message });
    }
  }
  
  // Final summary
  const passedTests = results.filter(r => r.success).length;
  const totalTests = results.length;
  
  console.log('\n' + '='.repeat(60));
  console.log('🎉 VISION POINT POSTER TESTS SUMMARY');
  console.log('='.repeat(60));
  console.log(`✅ Passed: ${passedTests}/${totalTests} tests`);
  console.log(`❌ Failed: ${totalTests - passedTests}/${totalTests} tests`);
  
  if (passedTests === totalTests) {
    console.log('\n🎊 ALL TESTS PASSED! Vision Point poster system is ready!');
    console.log('📂 Check ./output/posters/ for generated posters');
  } else {
    console.log('\n⚠️ Some tests failed. Please check the errors above.');
  }
  
  // Save test results
  const reportPath = path.join(__dirname, `poster_test_results_${Date.now()}.json`);
  await fs.writeFile(reportPath, JSON.stringify({
    timestamp: new Date().toISOString(),
    totalTests,
    passedTests,
    results
  }, null, 2));
  
  console.log(`📊 Test report saved: ${path.basename(reportPath)}`);
  
  return passedTests === totalTests;
}

// Run tests if called directly
if (require.main === module) {
  runComprehensiveTests()
    .then(success => {
      process.exit(success ? 0 : 1);
    })
    .catch(error => {
      console.error('❌ Test execution failed:', error);
      process.exit(1);
    });
}

